    center_point(point)
    {
        return [
            (point[0] - (this.width - 1) / 2),
            (point[1] - (this.height - 1) / 2),
            point[2]
        ];
    }

    get_lines()
    {
        var lines = [];
        for (let x = 0; x < this.width; x++)
        {
            for (let y = 0; y < this.height; y++)
            {
                if (x < this.width - 1)
                {
                    lines.push(
                        [
                            this.center_point([x, y, this.map[x][y]]),
                            this.center_point([x + 1, y, this.map[x + 1][y]])
                        ]
                    );
                }
                if (y < this.height - 1)
                {
                    lines.push(
                        [
                            this.center_point([x, y, this.map[x][y]]),
                            this.center_point([x, y + 1, this.map[x][y + 1]])
                        ]
                    );
                }
            }
        }
        return lines;
    }

